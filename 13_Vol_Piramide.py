# Asignando Variable
b = float(input("BASE, DE LA BASE DE LA PIRÁMIDE: "))
a = float(input("ALTURA, DE LA BASE DE LA PIRÁMIDE: "))
h = float(input("ALTURA DE LA PIRÁMIDE: "))

# Algoritmo
volumen = (1/3)*b*a*h

# Imprimimos Resultado
print(f"EL VOLUMEN DE LA PIRÁMIDE ES: {volumen} m3trs")